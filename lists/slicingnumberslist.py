#Given the list [“a”, “b”, “c”, “d”, “e”, “f”], use slicing to:
# Print the first three elements
# Print the last two elements
# Print every alternate element


letters = ["a","b","c","d","e","f"]

print("The first 3 elements in the list are:",letters[0:3])
print("The last 2 elements in the list are:",letters[-2:])
print("The alternate elements in the list are:",letters[::2])